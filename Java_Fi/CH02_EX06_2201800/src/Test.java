
public class Test {
	public static void main(String[] ar) {
		//자료형간 연산
		System.out.println(3+2);//5
		System.out.println(3-2);//1
		System.out.println(3*2);//6
		System.out.println(3/2);//1
		System.out.println((double)3/(double)2);//1.5
		System.out.println((double)3/2);//1.5
		System.out.println(3/2.0);//1.5
		System.out.println((double)(3/2));//1.0
		System.out.println((int)3.9+2.2);//5.2
		System.out.println((int)(3.9+2.2));//6
		System.out.println((double)(2.4+(int)5.7));//7.4
		
	}

}
